# ğŸ” Analyze & Store Sentiment in Batches
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def analyze_sentiment_and_store(batch_size: int = 500):
    news_ref = db.collection("news")
    docs = news_ref.stream()
    batch = db.batch()
    count = 0

    for doc in docs:
        data = doc.to_dict()
        doc_id = doc.id

        # Skip if already analyzed
        if data.get("sentiment_label") is not None and data.get("sentiment_score") is not None:
            continue

        content = (data.get("content") or "").strip()
        if not content:
            print(f"âš ï¸ Skipping empty content for {doc_id}")
            continue

        snippet = content[:512]
        try:
            result = analyze_snippet(snippet)
            label = result.get("label", "Neutral").capitalize()
            score = round(result.get("score", 0.0), 4)
        except Exception as e:
            print(f"âŒ Error analyzing sentiment for {doc_id}: {e}")
            continue

        batch.update(news_ref.document(doc_id), {
            "sentiment_label": label,
            "sentiment_score": score,
            "analyzed_at":     datetime.utcnow().isoformat() + "Z"
        })
        count += 1

        if count % batch_size == 0:
            batch.commit()
            batch = db.batch()

    if count % batch_size != 0:
        batch.commit()

    print(f"âœ… Sentiment updated for {count} articles.")

